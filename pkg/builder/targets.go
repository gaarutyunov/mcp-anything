// kongTarget generates a Kong Go plugin binary wrapping mcp-anything.
// The plugin starts mcp-anything as an embedded HTTP server and rewrites the
// Kong service address to route MCP traffic through the proxy.
//
// Requires github.com/Kong/go-pdk which is downloaded at build time.
var kongTarget = targetSpec{
	extraRequire:  []string{"github.com/Kong/go-pdk v0.11.0"},
	needsDownload: true,
	mainTemplate: `// Code generated by mcp-anything builder. DO NOT EDIT.
//
// Kong Go plugin that embeds mcp-anything as an HTTP handler.
// The plugin starts a local mcp-anything server on first request and
// rewrites the Kong service address so that Kong proxies MCP traffic
// through the embedded proxy.
package main

import (
	"context"
	"fmt"
	"log/slog"
	"net"
	"net/http"
	"sync"

	"github.com/Kong/go-pdk"
	"github.com/Kong/go-pdk/server"

	pkgconfig "github.com/gaarutyunov/mcp-anything/pkg/config"
	"github.com/gaarutyunov/mcp-anything/pkg/mcpanything"
{{- range .Packages}}
	_ "{{.}}"
{{- end}}
)

// Version is reported to Kong during plugin handshake.
const Version = "1.0.0"

// Priority determines the plugin execution order relative to other Kong plugins.
// Higher values run earlier in the access phase.
const Priority = 1000

// Config holds per-plugin configuration set via the Kong Admin API.
type Config struct {
	// ConfigPath is the path to the mcp-anything YAML config file.
	// When empty, the CONFIG_PATH environment variable is used, falling back
	// to /etc/mcp-anything/config.yaml.
	ConfigPath string ` + "`" + `json:"config_path"` + "`" + `
}

var (
	startOnce sync.Once
	proxyAddr string
	proxyErr  error
)

// New returns a fresh Config. Kong calls this for every request to obtain
// the plugin configuration.
func New() interface{} { return &Config{} }

// Access is called by Kong in the access phase for every proxied request.
// On the first call it starts the embedded mcp-anything server; subsequent
// calls reuse the already-running server.
func (c *Config) Access(kong *pdk.PDK) {
	startOnce.Do(func() {
		proxyAddr, proxyErr = startProxy(c.ConfigPath)
	})
	if proxyErr != nil {
		slog.Error("mcp-anything kong plugin: proxy startup failed", "error", proxyErr)
		if err := kong.Response.SetStatus(http.StatusInternalServerError); err != nil {
			slog.Error("setting status", "error", err)
		}
		return
	}
	// Rewrite the upstream service address to the embedded proxy.
	if err := kong.ServiceRequest.SetHeader("X-Forwarded-Host", proxyAddr); err != nil {
		slog.Error("mcp-anything kong plugin: setting upstream header", "error", err)
		if setErr := kong.Response.SetStatus(http.StatusBadGateway); setErr != nil {
			slog.Error("setting status", "error", setErr)
		}
	}
}

// startProxy starts mcp-anything listening on a random local port and returns
// the address (host:port).
func startProxy(cfgPath string) (string, error) {
	// Reserve a local port before starting the proxy so we know the address.
	ln, err := net.Listen("tcp", "127.0.0.1:0")
	if err != nil {
		return "", fmt.Errorf("reserving local port: %w", err)
	}
	addr := ln.Addr().String()
	if err := ln.Close(); err != nil {
		return "", fmt.Errorf("releasing port: %w", err)
	}

	var (
		path string
		cfg  *pkgconfig.ProxyConfig
	)
	if cfgPath != "" {
		path = cfgPath
		cfg, err = pkgconfig.Load(path)
	} else {
		path, cfg, err = mcpanything.LoadConfig()
	}
	if err != nil {
		return "", fmt.Errorf("loading config: %w", err)
	}

	// Override the listen address with the reserved port.
	cfg.Server.Addr = addr

	ctx := context.Background()
	proxy, err := mcpanything.New(ctx, cfg, mcpanything.WithConfigPath(path))
	if err != nil {
		return "", fmt.Errorf("creating proxy: %w", err)
	}
	proxy.StartBackground(ctx)
	go func() {
		if startErr := proxy.Start(ctx); startErr != nil && startErr != context.Canceled {
			slog.Error("mcp-anything kong plugin: proxy error", "error", startErr)
		}
	}()
	return addr, nil
}

func main() {
	server.StartServer(New, Version, Priority)
}
`,
}
